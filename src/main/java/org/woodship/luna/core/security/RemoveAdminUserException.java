package org.woodship.luna.core.security;

public class RemoveAdminUserException extends RuntimeException {

	public RemoveAdminUserException() {
		super();
	}

	public RemoveAdminUserException(String message, Throwable cause) {
		super(message, cause);
	}

	public RemoveAdminUserException(String message) {
		super(message);
	}

	public RemoveAdminUserException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = 5039732011142871489L;

}
